package model;
public class Candidato {
    //Atributos
    private String nomeCandidato,partidoCandidato;
    private int idCandidato;
    private boolean fichaLimpa;
    
    //Construtores
public Candidato() {
        
}       

    
    //SETs e GETs

    

    public String getNomeCandidato() {
        return nomeCandidato;
    }

    public void setNomeCandidato(String nomeCandidato) {
        this.nomeCandidato = nomeCandidato;
    }

    public String getPartidoCandidato() {
        return partidoCandidato;
    }

    public void setPartidoCandidato(String partidoCandidato) {
        this.partidoCandidato = partidoCandidato;
    }

    public int getIdCandidato() {
        return idCandidato;
    }

    public void setIdCandidato(int idCandidato) {
        this.idCandidato = idCandidato;
    }

    public boolean getFichaLimpa() {
        return true;
    }

    public void setFichaLimpa(boolean fichaLimpa) {
        this.fichaLimpa = true;
    }

 
}
    

   
    
    

